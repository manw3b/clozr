/** Helper para agrupar por stage en el render */
export function groupLeadsByStage(leads: Lead[]) {
  const grouped: Record<string, Lead[]> = {
    prospecto: [],
    contactado: [],
    'visita-agendada': [],
    presupuestado: [],
    negociando: [],
    cerrado: [],
    perdido: [],
  };
  for (const lead of leads) {
    const arr = grouped[lead.stage];
    if (arr) arr.push(lead);
  }
  // Ordenar cada columna por position
  for (const k of Object.keys(grouped)) {
    grouped[k].sort((a, b) => (a.position || 0) - (b.position || 0));
  }
  return grouped;
}
