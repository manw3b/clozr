function daysAgo(d: number) {
  return new Date(Date.now() - d * 86400_000).toISOString();
}
function hoursAgo(h: number) {
  return new Date(Date.now() - h * 3600_000).toISOString();
}
function inDays(d: number) {
  return new Date(Date.now() + d * 86400_000).toISOString();
}

